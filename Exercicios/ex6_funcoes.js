// a diferença do return vs console.log

function falaOla(){
  return "Olá"
}

console.log(falaOla())//"Olá"

function resultadoDoisMaisDois(){ //resultado_dois_mais_dois
  return 2+2 //4
}

console.log(resultadoDoisMaisDois()) //4
console.log(resultadoDoisMaisDois() > 5) //false
console.log(resultadoDoisMaisDois() * 20) //80


